package com.app.dto;

public class ApiResponse<T> {
    private boolean status=true;
    private T data;

    public ApiResponse(T data) {
        this.data = data;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}

